    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $filterShipped = request('shipped', 'all');
        if ($filterShipped == 'all') {
            $orders = \App\Order::all();
        } else if ($filterShipped == 'shipped') {
            $orders = \App\Order::whereNotNull('ship_date')->get();
        } else {
            $orders = \App\Order::whereNull('ship_date')->get();            
        }
        return view('resources.order.index')->with('orders', $orders)->with('filterShipped', $filterShipped);
    }

